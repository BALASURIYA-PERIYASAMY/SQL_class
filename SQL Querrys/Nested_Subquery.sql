## NESTED SUBQUERY

#FIND THE EMPLOYEES WHO EARN THE SAME SALARY AS THE HIGHEST PAID AS HR EMPLOYEE
# innermost query
SELECT MAX(SALARY) FROM EMPLOYEES2 WHERE DEPARTMENT = 'HR';

# middle query
SELECT SALARY FROM EMPLOYEES2 WHERE SALARY = ( SELECT MAX(SALARY) FROM EMPLOYEES2 WHERE DEPARTMENT = 'HR');

# outer query
SELECT EMP_NAME, DEPARTMENT, SALARY FROM EMPLOYEES2
	WHERE SALARY = ( SELECT SALARY FROM EMPLOYEES2 
		WHERE SALARY = ( SELECT MAX(SALARY) FROM EMPLOYEES2 WHERE DEPARTMENT = 'HR'));


# FIND THE EMPLOYEE IWHO WORK IN THE SAME CITY AS THE HIGHEST PAID EMPLOYEE
# innermost query
SELECT MAX(SALARY) FROM EMPLOYEES2;

# middle query
SELECT CITY FROM EMPLOYEES2 WHERE SALARY = (SELECT MAX(SALARY) FROM EMPLOYEES2);

# outer query
SELECT EMP_NAME, DEPARTMENT, CITY, SALARY FROM EMPLOYEES2 
	WHERE CITY=(SELECT CITY FROM EMPLOYEES2 
		WHERE SALARY = (SELECT MAX(SALARY) FROM EMPLOYEES2));



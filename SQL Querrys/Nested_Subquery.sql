## ANY - 
# EMPLOYEES WHOSE SALARY GREATER THAN ANY HR EMPLOYEES
SELECT SALARY FROM EMPLOYEES2 WHERE DEPARTMENT= "HR";

SELECT EMP_NAME, DEPARTMENT, SALARY FROM EMPLOYEES2 
	WHERE SALARY > ANY(SELECT SALARY FROM EMPLOYEES2 WHERE DEPARTMENT= "HR");

## ALL
#EMPLOYEES WHOSE SALARY IS GREATER THAN ALL HR EMPLOYEES
SELECT SALARY FROM EMPLOYEES2 WHERE DEPARTMENT= "HR";

SELECT EMP_NAME, DEPARTMENT, SALARY FROM EMPLOYEES2 
	WHERE SALARY > ALL(SELECT SALARY FROM EMPLOYEES2 WHERE DEPARTMENT= "HR");
    
#EMPLOYEES WHO EARN LESS THAN ALL IT EMPLOYEES
SELECT SALARY FROM EMPLOYEES2 WHERE DEPARTMENT= "IT";

SELECT EMP_NAME, DEPARTMENT, SALARY FROM EMPLOYEES2 
	WHERE SALARY < ALL(SELECT SALARY FROM EMPLOYEES2 WHERE DEPARTMENT= "IT");
    
#FIND THE EMPLOYEE WHOSE SALARY IS EQUAL TO ANY AVG SALARY OF EACH DEPARTMENT 

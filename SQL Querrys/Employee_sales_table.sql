## RANK()
#SHOW EMPLOYEES AND THEIR RANK BASED ON SALES IN EACH DEPARTMENT
SELECT EMP_NAME, DEPARTMENT, SALES_AMOUNT, RANK() 
OVER (PARTITION BY DEPARTMENT ORDER BY SALES_AMOUNT DESC) AS RN
 FROM EMPLOYEE_SALES;
 
# Rank employees by sales_amount in descending order.
SELECT EMP_NAME,SALES_AMOUNT, RANK() 
OVER (ORDER BY SALES_AMOUNT DESC) AS RN 
FROM EMPLOYEE_SALES;

#Using RANK(), retrieve employees who got rank = 2 in each department.
SELECT * FROM(
SELECT EMP_NAME, DEPARTMENT, SALES_AMOUNT, RANK() 
OVER(PARTITION BY DEPARTMENT ORDER BY SALES_AMOUNT) AS RN
FROM EMPLOYEE_SALES) AS T WHERE RN=2;

#SHOW TOP 3 EMPLOYEES RANK 1TO 3 BY SALES IN EACH DEPARTMENT

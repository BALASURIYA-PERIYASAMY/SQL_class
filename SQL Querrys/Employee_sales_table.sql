SUM(SALES_AMOUNT) OVER (PARTITION BY DEPARTMENT ORDER BY EMP_ID ASC) AS TOTAL_AMOUNT
 FROM EMPLOYEE_SALES;

#MOVING AVERAGE SALES
SELECT EMP_ID, EMP_NAME, DEPARTMENT, SALES_AMOUNT, 
AVG(SALES_AMOUNT) OVER (PARTITION BY DEPARTMENT ORDER BY EMP_ID ASC) AS AVG_AMOUNT 
FROM EMPLOYEE_SALES;

# SEQUENTIAL COUNT OF EMPLOYES
SELECT EMP_ID, EMP_NAME, DEPARTMENT, SALES_AMOUNT, 
COUNT(*) OVER (PARTITION BY DEPARTMENT ORDER BY EMP_ID ASC) AS COUNTING 
FROM EMPLOYEE_SALES;

SELECT DEPARTMENT, COUNT(*) AS TOTAL_EMPLOYEES 
FROM EMPLOYEE_SALES GROUP BY DEPARTMENT 
ORDER BY DEPARTMENT ASC; 

#MAX() VALUES
SELECT EMP_ID, EMP_NAME, DEPARTMENT, SALES_AMOUNT, 
MAX(SALES_AMOUNT) OVER (PARTITION BY DEPARTMENT ORDER BY EMP_ID ASC) AS MAXIMUM_VALUES 
FROM EMPLOYEE_SALES;

#MIN() VALUES
SELECT EMP_ID, EMP_NAME, DEPARTMENT, SALES_AMOUNT, 
MIN(SALES_AMOUNT) OVER (PARTITION BY DEPARTMENT ORDER BY EMP_ID ASC) AS MINIMUM_VALUES 
FROM EMPLOYEE_SALES;

#ROUND()
SELECT EMP_ID, EMP_NAME, DEPARTMENT, SALES_AMOUNT, 
ROUND(MIN(SALES_AMOUNT) OVER (PARTITION BY DEPARTMENT ORDER BY EMP_ID ASC)) 
AS MINIMUM_VALUES FROM EMPLOYEE_SALES;

## LEAD()
# SHOW THE NEXT EMPLOYEE SALES BASED ON OREDRING
SELECT EMP_ID, EMP_NAME, DEPARTMENT, SALES_AMOUNT, 
LEAD(SALES_AMOUNT) OVER(PARTITION BY DEPARTMENT ORDER BY EMP_ID ASC) AS NEXT_VALUE 
FROM EMPLOYEE_SALES;

## LAG()
SELECT EMP_ID, EMP_NAME, DEPARTMENT, SALES_AMOUNT, 
LAG(SALES_AMOUNT) OVER(PARTITION BY DEPARTMENT ORDER BY EMP_ID ASC) AS NEXT_VALUE 
FROM EMPLOYEE_SALES;


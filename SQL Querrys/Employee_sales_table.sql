## ROW_NUMBER()
# Write a query to assign a unique row number to each employee ordered by sales_amount in descending order.
SELECT EMP_NAME, SALES_AMOUNT, 
ROW_NUMBER() OVER(ORDER BY SALES_AMOUNT DESC) AS ROW_NUM
FROM EMPLOYEE_SALES;

#SHOW THE ROW NUMBER OF EACH WITHIN THE DEPARTMENT BASED ON DESC ORDER
SELECT EMP_ID, EMP_NAME, DEPARTMENT, SALES_AMOUNT, ROW_NUMBER() 
OVER(PARTITION BY DEPARTMENT ORDER BY SALES_AMOUNT DESC) AS ROW_NUM 
FROM EMPLOYEE_SALES;

#GIVE THE ROW NUMBER TO EACH EMPLOYEE IN THEIR DEPARTMENT (HIGHEST SALES FIRST)
SELECT EMP_ID, EMP_NAME, DEPARTMENT, SALES_AMOUNT, ROW_NUMBER() 
OVER(PARTITION BY DEPARTMENT ORDER BY SALES_AMOUNT DESC) AS ROW_NUM 
FROM EMPLOYEE_SALES;

#DISPLAY ONLY TOP 3 EMPLOYEES FOR EACH DEPARTMENT BY USING ROW_NUMBER()
SELECT * FROM (
SELECT EMP_NAME, DEPARTMENT, SALES_AMOUNT, ROW_NUMBER() 
OVER(PARTITION BY DEPARTMENT ORDER BY SALES_AMOUNT DESC) AS ROW_NUM
FROM EMPLOYEE_SALES) AS T 
WHERE ROW_NUM<=3;
 
#Using ROW_NUMBER(), retrieve the top 2 highest sales employees in each department.
SELECT * FROM (
SELECT EMP_NAME, DEPARTMENT, SALES_AMOUNT, ROW_NUMBER() 
OVER (PARTITION BY DEPARTMENT ORDER BY SALES_AMOUNT)AS RN FROM 
EMPLOYEE_SALES) 
AS T WHERE RN<=2; 

#FIND THE EMPLOYEES WITH THE SECOND HIGHEST SALES IN EACH NUMBER

## LIKE OPERATOR
#FIND THE EMPLOYEE WHOSE NAMES STRATS WITH 'R';
SELECT * FROM EMPLOYEES WHERE NAME LIKE 'R%';

#FIND THE EMPLOYEE WHOSE NAMES STRATS WITH 'A';
SELECT * FROM EMPLOYEES WHERE NAME LIKE 'A%';

#FIND THE EMPLOYEE WHOSE NAMES ENDS WITH 'A';
SELECT * FROM EMPLOYEES WHERE NAME LIKE '%A';

#FIND THE EMPLOYEES WHOSE NAMES CONTAINS THE WORD OF 'SINGH';
SELECT * FROM EMPLOYEES WHERE NAME LIKE "%SINGH%";

#FIND THE EMPLOYEES WHOSE DESIGNATION STARTS WITH 'MAN'
SELECT * FROM EMPLOYEES WHERE DESIGNATION LIKE "MAN%";

#FIND THE EMPLOYEES WHOSE DEPARTMENT NAME CONTAINS THE LETTER 'I'
SELECT * FROM EMPLOYEES WHERE DEPARTMENT LIKE "%I%";


## NOT LIKE

#FIND THE EMPLOYEES WHOSE NAMES DO NOT STARTS WITH 'R'
SELECT * FROM EMPLOYEES WHERE NAME NOT LIKE 'R%';

#FIND THE EMPLOYEES WHO DEPARTMENT DOES NOT CONTAIN THE 'I'
SELECT * FROM EMPLOYEES WHERE DEPARTMENT NOT LIKE '%I%';
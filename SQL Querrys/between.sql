## BETWEEN

#FIND THE EMPLOYEE WHOSE SALARY IS BETWEEN 50000 AND 70000
SELECT * FROM EMPLOYEES WHERE SALARY BETWEEN 50000 AND 70000;

#FIND THE EMPLOYEES WHOSE AGE IS BETWEEN 25 AND 30 YEARS
SELECT * FROM EMPLOYEES WHERE AGE BETWEEN 25 AND 30;

#FIND THE EMPLOYEES WHOSE HIRE DATE IS BETWEEN 2018 AND 2020
SELECT * FROM EMPLOYEES WHERE YEAR(HIRE_DATE) BETWEEN 2018 AND 2020;
SELECT * FROM EMPLOYEES WHERE HIRE_DATE BETWEEN '2018-01-01' AND '2020-12-31';

#FIND THE EMPLOYEES WHOSE AGE IS BETWEEN 30 AND 40 AND DEPARTMENT IS SALES
SELECT * FROM EMPLOYEES WHERE (AGE BETWEEN 30 AND 40) AND DEPARTMENT='SALES';

#FIND THE EMPLOYEES WHOSE EXPERIENCE BETWEEN 0 AND 3 YEAR AND CITY IS BANGALURU
SELECT * FROM EMPLOYEES WHERE (EXPERIENCE BETWEEN 0 AND 3) AND CITY= 'BENGALURU';



## NOT BETWEEN 

#FIND THE EMPLOYEES WHOSE EXPERIENCE IS NOT BETWEEN 2 AND 6 YEARS
SELECT * FROM EMPLOYEES WHERE EXPERIENCE NOT BETWEEN 2 AND 6;


## IN OPERATOR


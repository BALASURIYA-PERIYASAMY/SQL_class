### AGGIGATE FUNCTIONS
## COUNT
#COUNT TOTAL NUMBER OF SALES
SELECT COUNT(SALE_ID) FROM SALES_DATA;
SELECT COUNT(*) FROM SALES_DATA;

#COUNT HOW MANY DISTINCT PRODUCT WERE SOLD
SELECT COUNT(DISTINCT PRODUCT_NAME) FROM SALES_DATA;
SELECT COUNT(DISTINCT PRODUCT_NAME) AS UNIQUER_PRODUCTS FROM SALES_DATA;

## SUM
#FIND THE TOTAL QUANTITY OF THE PRODUCT SOLD
SELECT SUM(QUANTITY) AS TOTAL_QUANTITY FROM SALES_DATA;

#FIND THE TOTAL REVENUE
SELECT SUM(TOTAL_AMOUNT) AS TOTAL_REVENUE FROM SALES_DATA;

## AVG
#FIND THE AVERAGE QUANTITY PER SALE
SELECT AVG(QUANTITY) AS AVG_QUANTITY FROM SALES_DATA;

#FIND THE AVERAGE OF UNIT PRICE ACROSS ALL PRODUCTS
SELECT AVG(UNIT_PRICE) AS AVERAGE_UNIT_PRICE FROM SALES_DATA;
SELECT ROUND(AVG(UNIT_PRICE)) AS AVERAGE_UNIT_PRICE FROM SALES_DATA;
SELECT ROUND(AVG(UNIT_PRICE),2) AS AVERAGE_UNIT_PRICE FROM SALES_DATA;

## MAX
#FIND THE HIGHEST SALE AMOUNT
SELECT MAX(TOTAL_AMOUNT) AS HIGHEST_AMOUNT FROM SALES_DATA;

## MIN
#FIND THE LOWEST SALE AMOUNT
SELECT MIN(TOTAL_AMOUNT) AS LOWEST_AMOUNT FROM SALES_DATA;


# CALCULATE THE TOTAL REVENUE GENERATED BY SALESPERSON 'Bob'
SELECT SUM(TOTAL_AMOUNT) FROM SALES_DATA WHERE SALESPERSON ='BOB';

#FIND THE AVG SALE AMOUNT FOR ELECTRONICS CATOGRIES
SELECT AVG(TOTAL_AMOUNT) AS AVG_SALE_AMOUNT FROM SALES_DATA WHERE CATEGORY = 'ELECTRONICS'; 
SELECT ROUND(AVG(TOTAL_AMOUNT),2) AS AVG_SALE_AMOUNT FROM SALES_DATA WHERE CATEGORY = 'ELECTRONICS'; 

# FIND THE TOTAL REVENUE FOR THE EELECTRONICS CATEGORY IN CANADA
SELECT SUM(TOTAL_AMOUNT) AS TOTAL_REVENUE FROM SALES_DATA WHERE CATEGORY = 'ELECTRONICS' AND COUNTRY='CANADA'; 
 

## GROUP BY - GROUP THE ROWS THAT HAVE THE SAME VALUE IN ONE OR MORE COLUMNS SO THEN YOU CAN APPLY THE AGGRIGATE FUNCTION IN EACH ROW
#TOTAL REVENUE PER CATEGORY
SELECT CATEGORY,COUNT(CATEGORY), SUM(TOTAL_AMOUNT) AS TOTAL_REVENUE FROM SALES_DATA GROUP BY CATEGORY;

# TOTAL QUANTITY SOLD PER PRODUCTS
SELECT PRODUCT_NAME, SUM(QUANTITY) AS TOTAL_QUANTITY FROM SALES_DATA GROUP BY PRODUCT_NAME;

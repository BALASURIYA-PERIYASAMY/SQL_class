# Find diseases generating more than average revenue.
SELECT P.DISEASE, SUM(B.TOTAL_AMOUNT) AS REVENUE FROM PATIENTS P JOIN BILLING B ON P.PATIENT_ID = B.PATIENT_ID GROUP BY P.DISEASE HAVING SUM(B.TOTAL_AMOUNT) > (SELECT AVG(TOTAL_AMOUNT) FROM BILLING );

# Find patients generating more than average revenue.
SELECT P.NAME, SUM(B.TOTAL_AMOUNT) AS REVENUE FROM PATIENTS P JOIN BILLING B ON P.PATIENT_ID = B.PATIENT_ID GROUP BY P.NAME HAVING SUM(B.TOTAL_AMOUNT) > (SELECT AVG(TOTAL_AMOUNT) FROM BILLING );

# Find treatments generating more than average revenue.
SELECT B.TREATMENT, SUM(B.TOTAL_AMOUNT) AS REVENUE FROM PATIENTS P JOIN BILLING B ON P.PATIENT_ID = B.PATIENT_ID GROUP BY B.TREATMENT HAVING SUM(B.TOTAL_AMOUNT) > (SELECT AVG(TOTAL_AMOUNT) FROM BILLING );

# Find cumulative treatment revenue.
SELECT P.NAME, B.TREATMENT, SUM(B.TOTAL_AMOUNT) OVER(ORDER BY B.TREATMENT) AS CUMULATIVE_REVENUE FROM PATIENTS P JOIN BILLING B ON P.PATIENT_ID=B.PATIENT_ID;

# Find running average billing per treatment.
SELECT TREATMENT, TOTAL_AMOUNT, AVG(TOTAL_AMOUNT) OVER(PARTITION BY TREATMENT ORDER BY BILL_ID) AS RUNNING_AVG FROM BILLING;

# Find patients contributing top 20% revenue.
SELECT * FROM ( SELECT P.NAME, SUM(B.TOTAL_AMOUNT) AS REVENUE, NTILE(5) OVER(ORDER BY SUM(B.TOTAL_AMOUNT) DESC) AS GRP FROM PATIENTS P JOIN BILLING B ON P.PATIENT_ID = B.PATIENT_ID GROUP BY P.NAME ) X WHERE GRP = 1;

# Find patients contributing bottom 20% revenue.
SELECT * FROM ( SELECT P.NAME, SUM(B.TOTAL_AMOUNT) AS REVENUE, NTILE(5) OVER(ORDER BY SUM(B.TOTAL_AMOUNT)) AS GRP FROM PATIENTS P JOIN BILLING B ON P.PATIENT_ID = B.PATIENT_ID GROUP BY P.NAME ) X WHERE GRP = 1;

# Rank diseases based on revenue.
